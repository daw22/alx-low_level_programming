#include "3-calc.h"

/**
 * get_op_func - returns a pointer to a function for the appropriate operator
 * @s: string representation of the operator("+", "-", ..)
 *
 * Retutn: pointerto a func
 */
int (*get_op_func(char *s))(int, int)
{
	int i;
	op_t ops[] = {
		{"+", op_add},
		{"-", op_sub},
		{"*", op_mul},
		{"/", op_div},
		{"%", op_mod},
		{NULL, NULL}
	};

	i = 0;
	while (ops[i].op != NULL)
	{
		if (strcmp(s, ops[i].op) == 0)
			return (ops[i].f);
		i++;
	}
	return (NULL);
}
